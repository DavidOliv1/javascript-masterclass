/* A forma mais simples de converter um número é por meio da invocação da função construtora Number, passando um número no formato de
String e que pode estar em qualquer base. */

console.log(Number("10"));
console.log(Number("9.9"));
console.log(Number("0xFF"));
console.log(Number("0b10"));
console.log(Number("0o10"));
console.log(Number());
console.log(Number("JavaScript"));