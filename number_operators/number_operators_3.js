// Operadores de incremento e decremento

// ++ e --

// Podemos ter operações de pós e pré incremento de decremento. No pós incremento ou decremento, o número é retornado antes e incrementado ou decrementado depois. No pré incremento ou decremento, temos antes o incremento ou decremento e depois o retorno.

let result = 10;

console.log(result++);
console.log(++result);
console.log(result--);
console.log(--result);
